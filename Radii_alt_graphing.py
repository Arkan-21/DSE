# =============================================================================
# STAGNATION TEMPERATURE VS ALTITUDE
# MACH 5 FLIGHT IN STANDARD ISA
# =============================================================================

import numpy as np
import matplotlib.pyplot as plt
import matplotlib as mpl
from pathlib import Path

# =============================================================================
# PLOT STYLE
# =============================================================================

# =============================================================================
# FIGURE OUTPUT DIRECTORY
# =============================================================================

fig_dir = Path("figures")

fig_dir.mkdir(exist_ok=True)

mpl.rcParams.update({

    # Font
    "font.family": "serif",
    "font.size": 12,

    # Axes
    "axes.labelsize": 13,
    "axes.titlesize": 13,
    "axes.linewidth": 1.0,

    # Tick marks
    "xtick.labelsize": 11,
    "ytick.labelsize": 11,

    # Legend
    "legend.fontsize": 10,
    "legend.frameon": True,

    # Lines
    "lines.linewidth": 2.0,

    # Grid
    "grid.alpha": 0.3,
    "grid.linestyle": "--",

    # Figure export
    "savefig.dpi": 300,
    "savefig.bbox": "tight"
})

# =============================================================================
# PLOT STYLE
# =============================================================================



# =============================================================================
# CONSTANTS
# =============================================================================

gamma = 1.4

R = 287.0                    # [J/kg/K]

mach = 5.0

emissivity = 0.7

sigma = 5.670374419e-8       # Stefan-Boltzmann

# =============================================================================
# ISA MODEL
# =============================================================================

g0 = 9.80665                 # [m/s²]

T0 = 288.15                  # [K]

P0 = 101325.0                # [Pa]

L = -0.0065                  # [K/m]

# =============================================================================
# ISA ATMOSPHERE
# =============================================================================

def isa_properties(h):

    # Troposphere
    if h <= 11000:

        T = T0 + L * h

        P = P0 * (T / T0)**(-g0 / (L * R))

    # Lower stratosphere
    else:

        T = 216.65

        P11 = P0 * (216.65 / T0)**(-g0 / (L * R))

        P = P11 * np.exp(
            -g0 * (h - 11000)
            / (R * T)
        )

    rho = P / (R * T)

    return T, P, rho

# =============================================================================
# SUTTON-GRAVES HEATING
# =============================================================================

def stagnation_heating(rho_inf, u_inf, radius):

    return (
        1.74e-4
        * np.sqrt(rho_inf / radius)
        * u_inf**3
    )

# =============================================================================
# ALTITUDE RANGE
# =============================================================================

altitudes = np.linspace(
    25000,
    35000,
    250
)

# =============================================================================
# NOSE RADII
# =============================================================================

nose_radii = [
    0.003,   # 3 mm
    0.005,   # 5 mm
    0.010,   # 1 cm
    0.020,   # 2 cm
    0.050,   # 5 cm
    0.100    # 10 cm
]

# =============================================================================
# PLOT
# =============================================================================

fig, ax = plt.subplots(figsize=(7.5, 5.5))

for radius in nose_radii:

    T_stag_curve = []

    for h in altitudes:

        # -------------------------------------------------------------
        # ISA properties
        # -------------------------------------------------------------

        T_inf, P_inf, rho_inf = isa_properties(h)

        # -------------------------------------------------------------
        # Speed of sound
        # -------------------------------------------------------------

        a_inf = np.sqrt(
            gamma * R * T_inf
        )

        # -------------------------------------------------------------
        # Mach 5 velocity
        # -------------------------------------------------------------

        u_inf = mach * a_inf

        # -------------------------------------------------------------
        # Stagnation-point convective heating
        # -------------------------------------------------------------

        q_stag = stagnation_heating(
            rho_inf,
            u_inf,
            radius
        )

        # -------------------------------------------------------------
        # Radiative equilibrium temperature
        # -------------------------------------------------------------

        T_stag = (
            q_stag
            / (emissivity * sigma)
        )**0.25

        T_stag_curve.append(T_stag)

    # -----------------------------------------------------------------
    # Curve label formatting
    # -----------------------------------------------------------------

    if radius < 0.01:

        label = fr"$R_n = {radius*1000:.0f}\ \mathrm{{mm}}$"

    else:

        label = fr"$R_n = {radius*100:.0f}\ \mathrm{{cm}}$"

    ax.plot(
        T_stag_curve,
        altitudes / 1000,
        label=label
    )

# =============================================================================
# AXES
# =============================================================================

ax.set_xlabel("Stagnation Temperature [K]")

ax.set_ylabel("Altitude [km]")

ax.set_title(
    "Stagnation Temperature vs Altitude\n"
    "Mach 5 Flight in Standard ISA"
)

ax.grid(True)

ax.legend(loc="best")

ax.spines['top'].set_visible(False)

ax.spines['right'].set_visible(False)

fig.tight_layout()

# =============================================================================
# SAVE
# =============================================================================


fig.savefig(
    fig_dir / "stagnation_temperature_vs_altitude.pdf"
)
# =============================================================================

plt.show()